    public void showSalary(){
        String yr = year.getSelectedItem().toString();
        String mnt = "";
        int ind = month.getSelectedIndex();
        switch(ind){
            case 0: mnt = "01"; break;
            case 1: mnt = "02"; break;
            case 2: mnt = "03"; break;
            case 3: mnt = "04"; break;
            case 4: mnt = "05"; break;
            case 5: mnt = "06"; break;
            case 6: mnt = "07"; break;
            case 7: mnt = "08"; break;
            case 8: mnt = "09"; break;
            case 9: mnt = "10"; break;
            case 10: mnt = "11"; break;
            case 11: mnt = "12"; break;
        }
        String fulldate = yr+"-"+mnt;
        sql.getSalary(jTable1, fulldate);
    }
   
